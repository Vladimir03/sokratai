-- ============================================================================
-- Предметный снапшот AI-диалога ученика (subject-personalization Ф5, 2026-07-23)
-- ============================================================================
-- Свободный AI-чат (`chat/index.ts`) раньше не знал предмет разговора; у
-- ученика с двумя репетиторами предмет обязан быть ДЕТЕРМИНИРОВАН диалогом,
-- а не «случайным» profiles.difficult_subject (ревью A3 спеки). Снапшот
-- пишется при создании диалога (CreateChatDialog — опциональный селект;
-- общий чат намеренно NULL = без предметного контекста), сервер читает по
-- chatId с ownership-чеком user_id (anti-tamper) и валидирует значение по
-- словарю SUBJECT_LABELS_DENO на чтении — CHECK не нужен (additive-минимум,
-- невалидное значение просто игнорируется).

alter table public.chats add column if not exists subject text null;

comment on column public.chats.subject is
  'Канонический id предмета (src/types/homework.ts::SUBJECTS) — снапшот при '
  'создании диалога (subject-personalization Ф5). NULL = чат без предметного '
  'контекста. Сервер (chat/index.ts) валидирует значение на чтении.';
