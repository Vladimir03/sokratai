/**
 * Build chemistry rubric for the requested KIM number (or generic fallback).
 * Tutor rubric is prepended in the resolver, not here.
 */
export function getChemistryEgeMethodology(kimNumber: number | null | undefined): string {
  if (typeof kimNumber === "number" && KIM_METHODOLOGIES[kimNumber]) {
    return KIM_METHODOLOGIES[kimNumber];
  }
  return GENERIC_METHODOLOGY;
}

export function buildChemistryEgeRubric(kimNumber: number | null | undefined): Omit<SubjectRubric, "tutor_rubric_active" | "cefr_level" | "subject_label"> {
  return {
    role: ROLE,
    methodology: getChemistryEgeMethodology(kimNumber),
    hint_examples: HINT_EXAMPLES,
    fallback_hint: FALLBACK_HINT,
  };
}
