export function getMathEgeMethodology(kimNumber: number | null | undefined): string {
  if (typeof kimNumber === "number" && KIM_METHODOLOGIES[kimNumber]) {
    return KIM_METHODOLOGIES[kimNumber];
  }
  return GENERIC_METHODOLOGY;
}

export function buildMathEgeRubric(kimNumber: number | null | undefined): Omit<SubjectRubric, "tutor_rubric_active" | "cefr_level" | "subject_label"> {
  return {
    role: ROLE,
    methodology: getMathEgeMethodology(kimNumber),
    hint_examples: HINT_EXAMPLES,
    fallback_hint: FALLBACK_HINT,
  };
}
