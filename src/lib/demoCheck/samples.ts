/**
 * Демо-разбор «как Сократ проверяет работу» (v2.1 W1-A) — сдвиг aha влево.
 *
 * Курированный РЕАЛЬНЫЙ результат проверки (вердикт + балл + разбор), захваченный
 * один раз, чтобы репетитор увидел ценность за 30 секунд — ДО подключения
 * учеников (сегодня проверку невозможно увидеть без реального ученика + сдачи).
 * Рендерится теми же production-компонентами, что и настоящая проверка
 * (`PhysicsFlowchartTrace` / `CriteriaBreakdownTable`), поэтому демо честное:
 * это ровно то, что увидит ученик и репетитор.
 *
 * Ноль AI-вызова, детерминировано, бесплатно, без риска плохого первого
 * впечатления от live-вариативности (для live-проверки своей задачи — W1-B).
 *
 * Нейминг (rule 90 бан-словарь): «разбор Сократа», НЕ «AI-проверка».
 *
 * Охват: физика (A-сегмент пилота) первой. Другие предметы — контент-работа
 * (open question #1): добавь запись в SAMPLES + валидацию предметником.
 */
import type {
  HomeworkAiCriteriaItem,
  HomeworkFlowchartTrace,
} from "@/types/homework";

/** Вердикт демо-результата (подмножество GuidedCheckResult вердиктов). */
export type DemoCheckVerdict = "CORRECT" | "ON_TRACK" | "INCORRECT";

export interface DemoCheckSample {
  subject: string;
  /** Человекочитаемый бейдж-eyebrow, напр. «ЕГЭ · № 24 · развёрнутое решение». */
  examLabel: string;
  /** Условие задачи (может содержать LaTeX — рендерится через MathText). */
  taskText: string;
  /** Ответ «ученика» — намеренно неидеальный (показать, что ловит проверка). */
  studentAnswer: string;
  verdict: DemoCheckVerdict;
  score: number;
  maxScore: number;
  /** Дружелюбный разбор (то, что видит ученик). Anti-spoiler-safe. */
  feedback: string;
  /** Физика Часть 2 — визуальная трасса блок-схемы ФИПИ (или null). */
  flowchartTrace?: HomeworkFlowchartTrace | null;
  /** Языки/гуманитарные — покритериальная таблица (или null). */
  criteriaBreakdown?: HomeworkAiCriteriaItem[] | null;
}

